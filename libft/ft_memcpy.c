#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*desti;
	const unsigned char	*source;

	if (dest == src)
		return (dest);
	desti = (unsigned char *)dest;
	source = (const unsigned char *)src;
	while (n--)
		*(desti + n) = *(source + n);
	return (dest);
}
